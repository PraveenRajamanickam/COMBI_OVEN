void INIT_SETTING()
{
	unsigned char eeprom_address1,i;	
	for(i=0;i<=10;i++)
	{
		D_Setting[0]=eeprom_read(253);//253
		if(D_Setting[0]==DEFAULT_VALUE_SET)
		break;
	}
	if(D_Setting[0]!=DEFAULT_VALUE_SET)
	{	
		for(i=0;i<=253;i++)
		{
			eeprom_write(i,D_Setting1[i]);
		}
	}
	main_set_temp					=eeprom_read(eeram_cut_off_temperature);
	main_reset_temp					=eeprom_read(eeram_on_temperature);
	calibration_temperature				=eeprom_read(eeram_calibration_temperature);
	D_Setting[eeprom_address_refilling_on_time]	=eeprom_read(eeprom_address_refilling_on_time);
	caliberatiom_nagative_sign_flag			=eeprom_read(eeram_calibration_tempe_sign);
	brewing_on_time					=eeprom_read_int(eeram_brewing_on_time);
	//first_time_brewing_flg			=eeprom_read_int(eeram_perculation_time);
	interval_time					=eeprom_read_int(eeram_interval_time);
	brwwing_gram					=eeprom_read_int(eeram_brwwing_gram);
	brewing_dec_on_time				=eeprom_read_int(eeram_brewing_dec_on_time);
	
	cof_brewing_time				=eeprom_read_int(eeprom_cof_brewing_on_time);	
	
	gram_change_flg				=eeprom_read(eeram_gram_change_flg);	
	cb_100_cyl				=eeprom_read(eeram_brewing_cb_100_cyl);	
	cb_150_cyl				=eeprom_read(eeram_brewing_cb_150_cyl);	
	cb_200_cyl				=eeprom_read(eeram_brewing_cb_200_cyl);	
	cb_250_cyl				=eeprom_read(eeram_brewing_cb_250_cyl);
	
	
	if(brwwing_gram == 50)
		total_cb_cyl = cb_50_cyl;
	if(brwwing_gram == 100)
		total_cb_cyl = cb_100_cyl;
	if(brwwing_gram == 150)
		total_cb_cyl = cb_150_cyl;
	if(brwwing_gram == 200)
		total_cb_cyl = cb_200_cyl;
	if(brwwing_gram == 250)
		total_cb_cyl = cb_250_cyl;
	
	
	cof_brew_start_flg=coffee_brewing=eeprom_read(eeprom_cof_brew_start_flg);
	cof_brewing_cycle=cof_brew_remining_cly=eeprom_read(eeprom_cof_brew_remining_cly);
	
	save_cof_cups=eeprom_read_int(eeprom_save_cof_cups);
	
	
	
}